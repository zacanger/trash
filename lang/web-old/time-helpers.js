const time1minute = 1000 * 60
const time5minutes = 5 * time1minute
const time10minutes = 10 * time1minute
const time15minutes = 15 * time1minute
const time1hour = 60 * time1minute
const time1day = 24 * time1hour
const time1week = 7 * time1day
const time1year = 365 * time1day

module.exports = {
  time1minute,
  time5minutes,
  time10minutes,
  time15minutes,
  time1hour,
  time1day,
  time1week,
  time1year,
}
